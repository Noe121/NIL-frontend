/**
 * Default color when no school is affiliated or school not found
 */
export const DEFAULT_LOGO_COLOR = {
  primary: '#00D1FF', // Pantone 299 C - Original NILBx cyan
  secondary: '#0EA5E9', // sky-500 as fallback
  name: 'NILBx Default'
};

/**
 * Get school color for the superscript 'x' based on user's school
 * @param {string} schoolKey - The school identifier (e.g., 'alabama', 'ohio-state')
 * @param {boolean} useSecondary - Whether to use secondary color instead of primary
 * @returns {string} Hex color code
 */
export function getSchoolColor(schoolKey, useSecondary = false) {
  if (!schoolKey) {
    return DEFAULT_LOGO_COLOR.primary;
  }

  const normalizedKey = schoolKey.toLowerCase().replace(/\s+/g, '-');
  const schoolColors = SCHOOL_COLORS[normalizedKey];

  if (!schoolColors) {
    return DEFAULT_LOGO_COLOR.primary;
  }

  return useSecondary ? schoolColors.secondary : schoolColors.primary;
}

/**
 * Get school name from key
 * @param {string} schoolKey - The school identifier
 * @returns {string} Full school name
 */
export function getSchoolName(schoolKey) {
  if (!schoolKey) {
    return DEFAULT_LOGO_COLOR.name;
  }

  const normalizedKey = schoolKey.toLowerCase().replace(/\s+/g, '-');
  const schoolColors = SCHOOL_COLORS[normalizedKey];

  return schoolColors ? schoolColors.name : DEFAULT_LOGO_COLOR.name;
}

/**
 * Get all available schools
 * @returns {Array} Array of school objects with key and name
 */
export function getAllSchools() {
  return Object.entries(SCHOOL_COLORS).map(([key, value]) => ({
    key,
    name: value.name,
    primaryColor: value.primary,
    secondaryColor: value.secondary
  }));
}

/**
 * Search schools by name
 * @param {string} query - Search query
 * @returns {Array} Array of matching school objects
 */
export function searchSchools(query) {
  if (!query) return getAllSchools();

  const normalizedQuery = query.toLowerCase();
  return getAllSchools().filter(school =>
    school.name.toLowerCase().includes(normalizedQuery) ||
    school.key.includes(normalizedQuery)
  );
}
